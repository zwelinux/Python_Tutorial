def userinfo(name, age, address):
    print("your name is ", name)
    print("Your age is ", age)
    print("Your address is ", address)

# လူတစ်ယောက်စာအတွက် တစ်ကြိမ်
user1 = userinfo("wizard", 23, 'zwelinux@gmail.com')

user2 = userinfo("daniel", 20, 'danielisgenius@gmail.com')

# def -> keyword
# userinfo -> function name
# (name, age, address) -> parameter 
# print() -> func statement 
# var ထဲကို fun var -> obj 
# user1 -> object 
# userinfo() -> fun call 
# ("wizard", 23, 'zwelinux@gmail.com') -> argument 